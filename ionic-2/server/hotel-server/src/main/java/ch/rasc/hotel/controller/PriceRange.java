package ch.rasc.hotel.controller;

public class PriceRange {
	private int upper;
	private int lower;

	public int getUpper() {
		return this.upper;
	}

	public void setUpper(int upper) {
		this.upper = upper;
	}

	public int getLower() {
		return this.lower;
	}

	public void setLower(int lower) {
		this.lower = lower;
	}

}
